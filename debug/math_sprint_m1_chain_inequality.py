def chain_test(rho1, rho2, rho3, ent_fn):
    s12 = ent_fn(rho1, rho2)
    s23 = ent_fn(rho2, rho3)
    s13 = ent_fn(rho1, rho3)
    deficit = (s12 + s23) - s13           # > 0  iff chain inequality holds
    holds = deficit >= -1e-9               # numerical floor
    return dict(s12=s12, s23=s23, s13=s13, deficit=deficit, holds=bool(holds))


def panel_A_general_random(n_trials: int = 400, d: int = 4):
    """General random density matrices on C^d."""
    fails = 0
    deficits = []
    worst = None
    for _ in range(n_trials):
        rho1 = random_density(d)
        rho2 = random_density(d)
        rho3 = random_density(d)
        rec = chain_test(rho1, rho2, rho3, relative_entropy)
        deficits.append(rec["deficit"])
        if not rec["holds"]:
            fails += 1
        if worst is None or rec["deficit"] < worst["deficit"]:
            worst = rec
    arr = np.array(deficits)
    return dict(
        n_trials=n_trials,
        d=d,
        n_fail=fails,
        fail_rate=fails / n_trials,
        deficit_min=float(arr.min()),
        deficit_max=float(arr.max()),
        deficit_mean=float(arr.mean()),
        deficit_median=float(np.median(arr)),
        worst_case=worst,
    )


