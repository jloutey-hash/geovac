"""
Sprint M1 — Paper 49 Theorem 3.3 chain inequality diagnostic.

Tests whether the chain inequality
    S(rho_1 || rho_3)  <=  S(rho_1 || rho_2) + S(rho_2 || rho_3)         (CHAIN-S)
holds for quantum relative entropy S(rho || sigma) = Tr[rho(log rho - log sigma)].

Five panels:

Panel A — General random density matrices on a 2-qubit Hilbert space (4D).
         Tests CHAIN-S in maximum generality.  Expected: should fail freely
         (quantum relative entropy is not a metric and no general triangle
         inequality is known).

Panel B — Max-relative entropy D_max(rho||sigma) = log ||sigma^{-1/2} rho sigma^{-1/2}||_op
         on the same random triples.  Known to satisfy
             D_max(rho||tau)  <=  D_max(rho||sigma) + D_max(sigma||tau).      (CHAIN-Dmax)
         Acts as a positive control: CHAIN-Dmax should hold ALL the time.

Panel C — TICI-cocycle-orbit setup as in Paper 49 §3.
         Three KMS states sigma_1, sigma_2, sigma_3 generated by Hamiltonians
         H_1, H_2, H_3 on the same Hilbert space at temperature beta = 2 pi.
         Tests CHAIN-S on states sampled from each modular orbit
         {U(t) sigma_i U(-t)^dagger : t real}, where U(t) = exp(-i K t)
         and K is a chosen modular generator.  This mirrors the "three
         orbits, pairwise causally-related" setup of Theorem 3.3.

Panel D — Restricted to commuting Hamiltonians on the cocycle orbits.
         When [H_i, H_j] = 0 for all i,j the KMS states are mutually
         diagonal in a common basis and the problem reduces to classical
         Kullback-Leibler divergence.  Classical KL is also known not to
         satisfy a general triangle inequality, so we measure how often
         CHAIN-S fails in this reduced setting.

Panel E — Replacement candidates.
         (i)  Pinsker-type bound: ||rho-sigma||_1^2 <= 2 S(rho||sigma).
              Translate the Lorentzian-triangle question into a trace-norm
              statement and see if the chain inequality holds there.
         (ii) Connes-cocycle-bound surrogate:\ if the cocycle entropy
              production Delta S^{i->j} is replaced by D_max(sigma_i || sigma_j),
              does CHAIN hold and how does the deficit compare numerically?
         (iii) Whether the on-orbit single-KMS-state setting (Connes-Rovelli
               1994 original) survives:\  on a SINGLE orbit (all three states
               in the same orbit), the cocycle entropy production should
               vanish identically.  Sanity check.

All output written to debug/data/math_sprint_m1_chain_inequality.json so the
memo can quote exact numerics.
"""

