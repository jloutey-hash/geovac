def random_hermitian(d: int, scale: float = 1.0) -> np.ndarray:
    A = RNG.standard_normal((d, d)) + 1j * RNG.standard_normal((d, d))
    H = 0.5 * (A + A.conj().T)
    return scale * H


def random_density(d: int, purity: str = "mixed") -> np.ndarray:
    """Random full-rank density matrix on C^d via Wishart construction.

    purity='mixed' gives a generic full-rank state (the relevant regime for
    quantum relative entropy, since S(rho||sigma) = +inf when supp rho is not
    contained in supp sigma)."""
    A = RNG.standard_normal((d, d)) + 1j * RNG.standard_normal((d, d))
    rho = A @ A.conj().T
    rho = rho / np.trace(rho).real
    # Hermitize numerically.
    rho = 0.5 * (rho + rho.conj().T)
    # Floor eigenvalues for numerical stability.
    w, V = np.linalg.eigh(rho)
    w = np.clip(w, 1e-12, None)
    w = w / w.sum()
    rho = (V * w) @ V.conj().T
    return 0.5 * (rho + rho.conj().T)


def thermal_state(H: np.ndarray, beta: float) -> np.ndarray:
    rho = expm(-beta * H)
    Z = np.trace(rho).real
    rho = rho / Z
    return 0.5 * (rho + rho.conj().T)


def relative_entropy(rho: np.ndarray, sigma: np.ndarray) -> float:
    """S(rho || sigma) = Tr[rho (log rho - log sigma)].

    Computed in eigenbasis of rho to avoid the off-diagonal artifacts of
    direct logm of a non-diagonal matrix difference."""
    # Eigendecompose both — they share no basis in general.
    wr, Vr = np.linalg.eigh(rho)
    ws, Vs = np.linalg.eigh(sigma)
    # Floor for numerical stability.
    wr = np.clip(wr, 1e-15, None)
    ws = np.clip(ws, 1e-15, None)
    log_rho = (Vr * np.log(wr)) @ Vr.conj().T
    log_sigma = (Vs * np.log(ws)) @ Vs.conj().T
    val = np.trace(rho @ (log_rho - log_sigma)).real
    return float(val)


def max_relative_entropy(rho: np.ndarray, sigma: np.ndarray) -> float:
    """D_max(rho||sigma) = log lambda_max(sigma^{-1/2} rho sigma^{-1/2})."""
    ws, Vs = np.linalg.eigh(sigma)
    ws = np.clip(ws, 1e-15, None)
    sigma_inv_half = (Vs * (1.0 / np.sqrt(ws))) @ Vs.conj().T
    M = sigma_inv_half @ rho @ sigma_inv_half
    M = 0.5 * (M + M.conj().T)
    lam = float(np.max(np.linalg.eigvalsh(M)))
    return float(np.log(max(lam, 1e-300)))


def trace_distance(rho: np.ndarray, sigma: np.ndarray) -> float:
    diff = rho - sigma
    diff = 0.5 * (diff + diff.conj().T)
    w = np.linalg.eigvalsh(diff)
    return 0.5 * float(np.sum(np.abs(w)))


def modular_orbit_state(sigma: np.ndarray, K: np.ndarray, t: float) -> np.ndarray:
    """Modular evolution sigma_t = exp(-i K t) sigma exp(+i K t)."""
    U = expm(-1j * K * t)
    out = U @ sigma @ U.conj().T
    return 0.5 * (out + out.conj().T)


